package fr.viewpoint.client.storage;

public class StorageException extends RuntimeException {

	/**
	 * 
	 */
	private static final long serialVersionUID = -7108509343566454202L;

	public StorageException() {
		super();
	}

	public StorageException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public StorageException(String arg0) {
		super(arg0);
	}

	public StorageException(Throwable arg0) {
		super(arg0);
	}

}
